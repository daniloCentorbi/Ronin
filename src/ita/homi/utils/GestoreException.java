package ita.homi.utils;

public class GestoreException  extends Exception{

		private static final long serialVersionUID = 1L;

		public GestoreException() {
			super();
		}

		public GestoreException(String arg0, Throwable arg1) {
			super(arg0, arg1);
		}

		public GestoreException(String arg0) {
			super(arg0);
		}

		public GestoreException(Throwable arg0) {
			super(arg0);
		}
	}
		
